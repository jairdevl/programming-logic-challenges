# Convert the unidaes of length

meters = float(input("Enter the length in meters: "))

kilometers = meters * 0.001
centimeters = meters * 100
milimeters = meters * 1000

print(f"{meters} meters is equal to {kilometers} kilometers.")
print(f"{meters} meters is equal to {centimeters} centimeters.")
print(f"{meters} meters is equal to {milimeters} milimeters.")
